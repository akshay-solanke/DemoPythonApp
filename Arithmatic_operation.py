
#------------------Input--------------------
Value_1 = int(input('Please enter a two numbers to perform Arithmetic operation\n'))
Value_2 = int(input())

#------------------Arithmetic operation-------------------------
print("Addition =", (Value_1 + Value_2))               # Addition
print("Subtraction = ", (Value_1 - Value_2))           # Subtraction
print("Multiplication = ", (Value_1 * Value_2))        # Multiplication
print("Division as a float = ", (Value_1 / Value_2))   # Division to get result as a float value
print("Division as a int = ", (Value_1 // Value_2))    # Division to get result as a only integer value
print("Modulus = ", (Value_1 % 3))                     # Modulus to get remainder of division
print("Exponent = ", (Value_1 ** 3))                    # Exponent(Power of something))