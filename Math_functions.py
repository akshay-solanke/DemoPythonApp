#-------------------Basic_operations-------------------
x = 2.9
y = -2.3
print('Round of x is = ', round(x))     # Rounding the value
print('Round of y is = ', round(x))     # Rounding the value
print('Round of y is = ', abs(y))       # Return's Positive value if it is negative
x += 1                                  # Incrementing value
print('Incrementing value of x by 1 = ', x)
#-------------------Use_inbuilt_(math)_module-------------------
import math
